<p style='margin-left:1%;'> This application (Adult foster home web) has been developed to support service 
delivered by providers/operators. It has three basic areas: a) Summaries and reports of records such as Staff documentation
 (expiration date of certifications), residents appointment reminders and medication record keeping and tracking, etc. (b) the 
 second part is used to add new data, update existing data, and/or delete data, 
(c) the third part uses for displaying and viewing a printable) forms such updated physician records, disposal forms, medication administration (MAR), progress notes, resident 
financial information, etc. </p>
